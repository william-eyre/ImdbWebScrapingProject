package com.william.model;

public class Actors{

    private String name;
    private String character;

    public String getName() {
        return name;
    }

    public void setName(String newName) {
        name = newName;
    }

    public String getCharacter() {
        return character;
    }

    public void setCharacter(String newCharacter) {
        character = newCharacter;
    }

}
